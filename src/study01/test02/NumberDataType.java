package study01.test02;

public class NumberDataType {

	public static void main(String[] args) {
		byte b=-128; //byte= -128~127까지 가능
		short s=128;
		int i=10000000; //가장 많이 사용 되는 정수 데이터타입
		long l=1000000000; //0이 9개
			
		double d=1;
		float f=12345.97f; //float에서 소수를 나타낼때, f를 뒤에 붙여준다
		
		System.out.println(d+f);
	}
}
